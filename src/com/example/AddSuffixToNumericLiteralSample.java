package com.example;

public class AddSuffixToNumericLiteralSample {
    public static void main(String[] args) {
        System.out.println(2200000000L);  // 2200000000 long
        System.out.println(3.2);  // 3.2 double
        System.out.println(3.2D);  // 3.2 double
        System.out.println(3.2F);  // 3.2 float
    }
}

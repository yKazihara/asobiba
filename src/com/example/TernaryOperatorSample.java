package com.example;

public class TernaryOperatorSample {
    public static void main(String[] args) {
        int score;

        score = 70;
        System.out.print(STR."Score=\{score}:");
        System.out.println(score > 70 ? "OK" : "NG"); // NG

        score = 84;
        System.out.print(STR."Score=\{score}:");
        System.out.println(score > 70 ? "OK" : "NG"); // OK
    }
}
